    /// Build a realistic 2-layer model: Dense(1,16)+ReLU → Dense(16,3)
    fn create_two_layer_weights() -> QuantizedModelWeights {
        // layer1 weights: 1 input × 16 outputs
        let layer1_weights: Vec<i8> = (0..16_i8)
            .map(|i| if i % 2 == 0 { i + 1 } else { -(i + 1) })
            .collect();
        // layer2 weights: 16 inputs × 3 outputs = 48 values
        let layer2_weights: Vec<i8> = (0..48_i8)
            .map(|i| if i % 3 == 0 { i + 2 } else { -(i + 2) })
            .collect();

        QuantizedModelWeights {
            layers: vec![
                QuantizedLayerWeights {
                    name: "dense1".to_string(),
                    layer_type: LayerType::Dense,
                    input_size: 1,
                    output_size: 16,
                    weights_int8: layer1_weights,
                    bias_int32: (0..16).map(|i| i * 10).collect(),
                    weight_scale: 0.0078125,
                    weight_zero_point: 0,
                    output_scale: 0.015625,
                    output_zero_point: 0,
                    has_relu: true,
                },
                QuantizedLayerWeights {
                    name: "dense2".to_string(),
                    layer_type: LayerType::Dense,
                    input_size: 16,
                    output_size: 3,
                    weights_int8: layer2_weights,
                    bias_int32: vec![5, -10, 15],
                    weight_scale: 0.00390625,
                    weight_zero_point: 0,
                    output_scale: 0.0078125,
                    output_zero_point: 0,
                    has_relu: false,
                },
            ],
            input_scale: 0.003921568,
            input_zero_point: 0,
            input_min: -200.0,
            input_max: 200.0,
            num_classes: 3,
            class_labels: vec![
                "class_a".to_string(),
                "class_b".to_string(),
                "class_c".to_string(),
            ],
        }
    }

    fn unique_dir(suffix: &str) -> std::path::PathBuf {
        use std::time::{SystemTime, UNIX_EPOCH};
        let ts = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .map(|d| d.subsec_nanos())
            .unwrap_or(0);
        std::env::temp_dir().join(format!(
            "blitzed_esp32_{}_{}_{}",
            suffix,
            std::process::id(),
            ts
        ))
    }

