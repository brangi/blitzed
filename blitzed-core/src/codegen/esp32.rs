//! ESP32-specific code generator
//!
//! Generates complete, compilable ESP-IDF v5.x projects with:
//! - Real INT8 quantized model weights
//! - Layer-by-layer inference kernel
//! - Proper CMakeLists.txt with idf_component_register
//! - Sensor integration boilerplate

use super::{CodeGenerator, GeneratedCode};
use crate::model::{LayerType, QuantizedLayerWeights, QuantizedModelWeights};
use crate::{Model, Result};
use std::path::Path;

/// ESP32 code generator
pub struct Esp32CodeGen;

impl Esp32CodeGen {
    #[must_use]
    pub const fn new() -> Self {
        Self
    }

    /// Generate a complete ESP-IDF project from quantized weights.
    ///
    /// This produces a project that compiles with `idf.py build` and
    /// runs real INT8 inference on the ESP32.
    pub fn generate_from_weights(
        &self,
        weights: &QuantizedModelWeights,
        output_dir: &Path,
    ) -> Result<GeneratedCode> {
        let main_dir = output_dir.join("main");
        std::fs::create_dir_all(&main_dir)?;

        // Generate all project files
        let project_cmake = self.gen_project_cmake();
        let main_cmake = self.gen_main_cmake();
        let sdkconfig = self.gen_sdkconfig_defaults();
        let weights_header = self.gen_weights_header(weights);
        let inference_header = self.gen_inference_header(weights);
        let inference_impl = self.gen_inference_impl(weights);
        let main_c = self.gen_main_c(weights);

        // Write files
        std::fs::write(output_dir.join("CMakeLists.txt"), &project_cmake)?;
        std::fs::write(main_dir.join("CMakeLists.txt"), &main_cmake)?;
        std::fs::write(output_dir.join("sdkconfig.defaults"), &sdkconfig)?;
        std::fs::write(main_dir.join("blitzed_model_weights.h"), &weights_header)?;
        std::fs::write(main_dir.join("blitzed_inference.h"), &inference_header)?;
        std::fs::write(main_dir.join("blitzed_inference.c"), &inference_impl)?;
        std::fs::write(main_dir.join("main.c"), &main_c)?;

        Ok(GeneratedCode {
            implementation_file: inference_impl,
            header_file: Some(weights_header),
            example_file: Some(main_c),
            build_config: Some(project_cmake),
            dependencies: vec!["ESP-IDF v5.x".to_string()],
        })
    }

    fn gen_project_cmake(&self) -> String {
        format!(
            r#"# Generated by Blitzed v{version}
# ESP-IDF project - build with: idf.py set-target esp32 && idf.py build

cmake_minimum_required(VERSION 3.16)

include($ENV{{IDF_PATH}}/tools/cmake/project.cmake)
project(blitzed_model)
"#,
            version = crate::VERSION
        )
    }

    fn gen_main_cmake(&self) -> String {
        format!(
            "# Generated by Blitzed v{}\n\
             idf_component_register(SRCS \"main.c\" \"blitzed_inference.c\"\n\
             {}INCLUDE_DIRS \".\")\n",
            crate::VERSION,
            "                       "
        )
    }

    fn gen_sdkconfig_defaults(&self) -> String {
        "# Generated by Blitzed\n\
         # ESP32 optimized for inference\n\
         CONFIG_ESP32_DEFAULT_CPU_FREQ_240=y\n\
         CONFIG_FREERTOS_HZ=1000\n\
         CONFIG_LOG_DEFAULT_LEVEL_INFO=y\n\
         CONFIG_ESP_SYSTEM_EVENT_TASK_STACK_SIZE=4096\n"
            .to_string()
    }

    fn gen_weights_header(&self, weights: &QuantizedModelWeights) -> String {
        let mut out = format!(
            "// Generated by Blitzed v{}\n\
             // INT8 quantized model weights\n\
             \n\
             #ifndef BLITZED_MODEL_WEIGHTS_H\n\
             #define BLITZED_MODEL_WEIGHTS_H\n\
             \n\
             #include <stdint.h>\n\n",
            crate::VERSION
        );

        // Input quantization parameters
        out.push_str(&format!(
            "// Input quantization\n\
             #define INPUT_SCALE {:.10}f\n\
             #define INPUT_ZERO_POINT {}\n\
             #define INPUT_MIN {:.1}f\n\
             #define INPUT_MAX {:.1}f\n\n",
            weights.input_scale, weights.input_zero_point, weights.input_min, weights.input_max
        ));

        // Class labels
        out.push_str(&format!("#define NUM_CLASSES {}\n", weights.num_classes));
        out.push_str("static const char* class_labels[] = {");
        for (i, label) in weights.class_labels.iter().enumerate() {
            if i > 0 {
                out.push_str(", ");
            }
            out.push_str(&format!("\"{}\"", label));
        }
        out.push_str("};\n\n");

        // Per-layer weights, biases, and quantization parameters
        for layer in &weights.layers {
            out.push_str(&format!(
                "// {}: {}({}, {}){}\n",
                layer.name,
                match layer.layer_type {
                    LayerType::Dense => "Dense",
                    LayerType::Conv2d => "Conv2d",
                },
                layer.input_size,
                layer.output_size,
                if layer.has_relu { " + ReLU" } else { "" }
            ));
            out.push_str(&format!(
                "#define {name}_INPUT_SIZE {input}\n\
                 #define {name}_OUTPUT_SIZE {output}\n",
                name = layer.name.to_uppercase(),
                input = layer.input_size,
                output = layer.output_size
            ));

            // Weights array
            out.push_str(&format!(
                "static const int8_t {}_weights[{}] = {{\n    ",
                layer.name,
                layer.weights_int8.len()
            ));
            for (i, &w) in layer.weights_int8.iter().enumerate() {
                if i > 0 && i % 16 == 0 {
                    out.push_str("\n    ");
                }
                out.push_str(&format!("{}, ", w));
            }
            out.push_str("\n};\n");

            // Bias array
            out.push_str(&format!(
                "static const int32_t {}_bias[{}] = {{\n    ",
                layer.name,
                layer.bias_int32.len()
            ));
            for (i, &b) in layer.bias_int32.iter().enumerate() {
                if i > 0 && i % 8 == 0 {
                    out.push_str("\n    ");
                }
                out.push_str(&format!("{}, ", b));
            }
            out.push_str("\n};\n");

            // Quantization parameters
            out.push_str(&format!(
                "static const float {name}_weight_scale = {ws:.10}f;\n\
                 static const int32_t {name}_weight_zero_point = {wzp};\n\
                 static const float {name}_output_scale = {os:.10}f;\n\
                 static const int32_t {name}_output_zero_point = {ozp};\n\n",
                name = layer.name,
                ws = layer.weight_scale,
                wzp = layer.weight_zero_point,
                os = layer.output_scale,
                ozp = layer.output_zero_point,
            ));
        }

        out.push_str("#endif // BLITZED_MODEL_WEIGHTS_H\n");
        out
    }

    fn gen_inference_header(&self, weights: &QuantizedModelWeights) -> String {
        let first_input = weights.layers.first().map_or(1, |l| l.input_size);
        let last_output = weights.layers.last().map_or(1, |l| l.output_size);

        format!(
            "// Generated by Blitzed v{version}\n\
             #ifndef BLITZED_INFERENCE_H\n\
             #define BLITZED_INFERENCE_H\n\
             \n\
             #include <stdint.h>\n\
             \n\
             #define MODEL_INPUT_SIZE {input}\n\
             #define MODEL_OUTPUT_SIZE {output}\n\
             \n\
             int blitzed_model_init(void);\n\
             int blitzed_model_predict(const int8_t* input, int input_len, int8_t* output, int output_len);\n\
             int8_t blitzed_quantize_input(float raw_value);\n\
             float blitzed_dequantize_output(int8_t value);\n\
             int blitzed_argmax(const int8_t* output, int len);\n\
             \n\
             #endif // BLITZED_INFERENCE_H\n",
            version = crate::VERSION,
            input = first_input,
            output = last_output,
        )
    }

    fn gen_inference_impl(&self, weights: &QuantizedModelWeights) -> String {
        let mut out = format!(
            "// Generated by Blitzed v{}\n\
             // Real INT8 inference kernel\n\
             \n\
             #include \"blitzed_inference.h\"\n\
             #include \"blitzed_model_weights.h\"\n\
             #include <string.h>\n\n",
            crate::VERSION
        );

        // model_init
        out.push_str(
            "int blitzed_model_init(void) {\n\
             \x20\x20\x20\x20return 0; // Weights are const arrays, no dynamic init needed\n\
             }\n\n",
        );

        // model_predict - generate layer-by-layer inference
        out.push_str(
            "int blitzed_model_predict(const int8_t* input, int input_len, int8_t* output, int output_len) {\n\
             \x20\x20\x20\x20if (!input || !output) return -1;\n\n",
        );

        let num_layers = weights.layers.len();
        for (i, layer) in weights.layers.iter().enumerate() {
            let is_first = i == 0;
            let is_last = i == num_layers - 1;
            let input_var = if is_first {
                "input".to_string()
            } else {
                format!("hidden_{}", i - 1)
            };
            let input_len_expr = if is_first {
                "input_len".to_string()
            } else {
                format!("{}_OUTPUT_SIZE", weights.layers[i - 1].name.to_uppercase())
            };
            let prev_scale = if is_first {
                "INPUT_SCALE".to_string()
            } else {
                format!("{}_output_scale", weights.layers[i - 1].name)
            };

            match layer.layer_type {
                LayerType::Dense => {
                    if !is_last {
                        // Hidden layer: output to local buffer
                        out.push_str(&format!(
                            "    // {name}: Dense({inp}, {outp}){relu}\n\
                             \x20\x20\x20\x20int8_t hidden_{i}[{name_upper}_OUTPUT_SIZE];\n\
                             \x20\x20\x20\x20for (int j = 0; j < {name_upper}_OUTPUT_SIZE; j++) {{\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20int32_t acc = {name}_bias[j];\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20for (int k = 0; k < {name_upper}_INPUT_SIZE && k < {in_len}; k++) {{\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20acc += (int32_t){in_var}[k] * (int32_t){name}_weights[k * {name_upper}_OUTPUT_SIZE + j];\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20}}\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20float fval = (float)acc * {prev_scale} * {name}_weight_scale;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20int32_t q = (int32_t)(fval / {name}_output_scale) + {name}_output_zero_point;\n",
                            name = layer.name,
                            name_upper = layer.name.to_uppercase(),
                            inp = layer.input_size,
                            outp = layer.output_size,
                            relu = if layer.has_relu { " + ReLU" } else { "" },
                            i = i,
                            in_var = input_var,
                            in_len = input_len_expr,
                            prev_scale = prev_scale,
                        ));
                        if layer.has_relu {
                            out.push_str(&format!(
                                "        if (q < {name}_output_zero_point) q = {name}_output_zero_point; // ReLU\n",
                                name = layer.name
                            ));
                        }
                        out.push_str(&format!(
                            "        if (q > 127) q = 127;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20if (q < -128) q = -128;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20hidden_{i}[j] = (int8_t)q;\n\
                             \x20\x20\x20\x20}}\n\n",
                            i = i
                        ));
                    } else {
                        // Output layer: write to output buffer
                        out.push_str(&format!(
                            "    // {name}: Dense({inp}, {outp}) — output layer\n\
                             \x20\x20\x20\x20int out_len = {name_upper}_OUTPUT_SIZE < output_len ? {name_upper}_OUTPUT_SIZE : output_len;\n\
                             \x20\x20\x20\x20for (int j = 0; j < out_len; j++) {{\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20int32_t acc = {name}_bias[j];\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20for (int k = 0; k < {name_upper}_INPUT_SIZE; k++) {{\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20\x20acc += (int32_t){in_var}[k] * (int32_t){name}_weights[k * {name_upper}_OUTPUT_SIZE + j];\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20}}\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20float fval = (float)acc * {prev_scale} * {name}_weight_scale;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20int32_t q = (int32_t)(fval / {name}_output_scale) + {name}_output_zero_point;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20if (q > 127) q = 127;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20if (q < -128) q = -128;\n\
                             \x20\x20\x20\x20\x20\x20\x20\x20output[j] = (int8_t)q;\n\
                             \x20\x20\x20\x20}}\n\n",
                            name = layer.name,
                            name_upper = layer.name.to_uppercase(),
                            inp = layer.input_size,
                            outp = layer.output_size,
                            in_var = input_var,
                            prev_scale = prev_scale,
                        ));
                    }
                }
                LayerType::Conv2d => {
                    // Conv2d codegen can be added later; for now tiny models use Dense only
                    out.push_str(&format!(
                        "    // TODO: Conv2d layer '{}' — not yet supported in codegen\n\n",
                        layer.name
                    ));
                }
            }
        }

        out.push_str("    return 0;\n}\n\n");

        // quantize_input
        out.push_str(
            "int8_t blitzed_quantize_input(float raw_value) {\n\
             \x20\x20\x20\x20float normalized = raw_value / INPUT_MAX;\n\
             \x20\x20\x20\x20if (normalized > 1.0f) normalized = 1.0f;\n\
             \x20\x20\x20\x20if (normalized < -1.0f) normalized = -1.0f;\n\
             \x20\x20\x20\x20int32_t q = (int32_t)(normalized / INPUT_SCALE) + INPUT_ZERO_POINT;\n\
             \x20\x20\x20\x20if (q > 127) q = 127;\n\
             \x20\x20\x20\x20if (q < -128) q = -128;\n\
             \x20\x20\x20\x20return (int8_t)q;\n\
             }\n\n",
        );

        // dequantize_output
        let last_layer = weights.layers.last();
        let last_scale =
            last_layer.map_or("0.01f".to_string(), |l| format!("{}_output_scale", l.name));
        let last_zp =
            last_layer.map_or("0".to_string(), |l| format!("{}_output_zero_point", l.name));
        out.push_str(&format!(
            "float blitzed_dequantize_output(int8_t value) {{\n\
             \x20\x20\x20\x20return ((float)value - {lzp}) * {ls};\n\
             }}\n\n",
            ls = last_scale,
            lzp = last_zp,
        ));

        // argmax
        out.push_str(
            "int blitzed_argmax(const int8_t* output, int len) {\n\
             \x20\x20\x20\x20int max_idx = 0;\n\
             \x20\x20\x20\x20for (int i = 1; i < len; i++) {\n\
             \x20\x20\x20\x20\x20\x20\x20\x20if (output[i] > output[max_idx]) max_idx = i;\n\
             \x20\x20\x20\x20}\n\
             \x20\x20\x20\x20return max_idx;\n\
             }\n",
        );

        out
    }

    fn gen_main_c(&self, weights: &QuantizedModelWeights) -> String {
        let num_classes = weights.num_classes;

        format!(
            r#"// Generated by Blitzed v{version}
// ESP32 inference demo with real sensor input

#include <stdio.h>
#include <freertos/FreeRTOS.h>
#include <freertos/task.h>
#include <esp_log.h>
#include <esp_timer.h>
#include <esp_system.h>
#include "blitzed_inference.h"
#include "blitzed_model_weights.h"

static const char* TAG = "blitzed";

static void run_benchmark(int iterations) {{
    int8_t input = 0;
    int8_t output[MODEL_OUTPUT_SIZE];
    int64_t total_us = 0;
    int64_t min_us = INT64_MAX;
    int64_t max_us = 0;

    for (int i = 0; i < iterations; i++) {{
        input = blitzed_quantize_input((float)(i % 400 - 200));
        int64_t start = esp_timer_get_time();
        blitzed_model_predict(&input, 1, output, MODEL_OUTPUT_SIZE);
        int64_t elapsed = esp_timer_get_time() - start;
        total_us += elapsed;
        if (elapsed < min_us) min_us = elapsed;
        if (elapsed > max_us) max_us = elapsed;
    }}

    ESP_LOGI(TAG, "=== Benchmark: %d iterations ===", iterations);
    ESP_LOGI(TAG, "  Mean: %lld us", total_us / iterations);
    ESP_LOGI(TAG, "  Min:  %lld us", min_us);
    ESP_LOGI(TAG, "  Max:  %lld us", max_us);
    ESP_LOGI(TAG, "  Free heap: %lu bytes", (unsigned long)esp_get_free_heap_size());
}}

void app_main(void) {{
    ESP_LOGI(TAG, "Blitzed v{version} — INT8 inference on ESP32");
    ESP_LOGI(TAG, "Model: {num_classes} classes, %d input, %d output",
             MODEL_INPUT_SIZE, MODEL_OUTPUT_SIZE);
    ESP_LOGI(TAG, "Free heap at start: %lu bytes", (unsigned long)esp_get_free_heap_size());

    blitzed_model_init();

    // Run benchmark first
    run_benchmark(10000);

    // Continuous inference loop using hall sensor
    ESP_LOGI(TAG, "Starting continuous inference (hall sensor)...");
    while (1) {{
        int hall_value = hall_sensor_read();
        int8_t input = blitzed_quantize_input((float)hall_value);
        int8_t output[MODEL_OUTPUT_SIZE];

        int64_t start = esp_timer_get_time();
        blitzed_model_predict(&input, 1, output, MODEL_OUTPUT_SIZE);
        int64_t elapsed_us = esp_timer_get_time() - start;

        int class_id = blitzed_argmax(output, MODEL_OUTPUT_SIZE);
        const char* label = (class_id < NUM_CLASSES) ? class_labels[class_id] : "unknown";

        ESP_LOGI(TAG, "Hall: %d -> %s (raw: [%d,%d,%d]) %lld us",
                 hall_value, label,
                 output[0], output[1], output[2 < MODEL_OUTPUT_SIZE ? 2 : MODEL_OUTPUT_SIZE - 1],
                 elapsed_us);

        vTaskDelay(pdMS_TO_TICKS(500));
    }}
}}
"#,
            version = crate::VERSION,
            num_classes = num_classes,
        )
    }
}

// Legacy CodeGenerator trait implementation for backwards compatibility
// Uses a synthetic model with placeholder weights
impl CodeGenerator for Esp32CodeGen {
    fn generate(&self, model: &Model, output_dir: &Path) -> Result<GeneratedCode> {
        // Build a minimal quantized weights struct from model metadata
        // so the old interface still works (generates a template project)
        let info = model.info();
        let input_size: usize = info
            .input_shapes
            .first()
            .map_or(1, |s| s.iter().product::<i64>() as usize);
        let output_size: usize = info
            .output_shapes
            .first()
            .map_or(1, |s| s.iter().product::<i64>() as usize);

        let placeholder = QuantizedModelWeights {
            layers: vec![QuantizedLayerWeights {
                name: "placeholder".to_string(),
                layer_type: LayerType::Dense,
                input_size,
                output_size,
                weights_int8: vec![0i8; input_size * output_size],
                bias_int32: vec![0i32; output_size],
                weight_scale: 0.01,
                weight_zero_point: 0,
                output_scale: 0.01,
                output_zero_point: 0,
                has_relu: false,
            }],
            input_scale: 0.003921568,
            input_zero_point: 0,
            input_min: -1.0,
            input_max: 1.0,
            num_classes: output_size,
            class_labels: (0..output_size).map(|i| format!("class_{}", i)).collect(),
        };

        self.generate_from_weights(&placeholder, output_dir)
    }

    fn target_name(&self) -> &'static str {
        "esp32"
    }

    fn dependencies(&self) -> Vec<String> {
        vec!["ESP-IDF v5.x".to_string()]
    }
}

impl Default for Esp32CodeGen {
    fn default() -> Self {
        Self::new()
    }
}

